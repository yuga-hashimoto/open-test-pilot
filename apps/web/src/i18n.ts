export type Locale = "en" | "ja";

const STORAGE_KEY = "opentestpilot-locale";

export function detectBrowserLocale(navigatorLanguage: string | undefined = typeof navigator === "undefined" ? undefined : navigator.language): Locale {
  return navigatorLanguage?.toLowerCase().startsWith("ja") === true ? "ja" : "en";
}

function readStoredLocale(): Locale | undefined {
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    return stored === "en" || stored === "ja" ? stored : undefined;
  } catch {
    return undefined;
  }
}

function storeLocale(locale: Locale): void {
  try {
    localStorage.setItem(STORAGE_KEY, locale);
  } catch {
    /* localStorage may be unavailable (private browsing); locale just won't persist */
  }
}

