const dictionaries: Record<Locale, Dictionary> = { en, ja };

function interpolate(template: string, params: Record<string, string | number> | undefined): string {
  if (params === undefined) return template;
  return template.replace(/\{(\w+)\}/g, (match, key: string) => (key in params ? String(params[key]) : match));
}

export function translate(locale: Locale, key: string, params?: Record<string, string | number>): string {
  const template = dictionaries[locale][key] ?? dictionaries.en[key] ?? key;
  return interpolate(template, params);
}

export interface LocaleApi {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: string, params?: Record<string, string | number>) => string;
}

export function useLocaleState(): LocaleApi {
  const [locale, setLocaleState] = useState<Locale>(() => readStoredLocale() ?? detectBrowserLocale());
  const setLocale = (next: Locale) => {
    setLocaleState(next);
    storeLocale(next);
  };
  const t = useMemo(() => (key: string, params?: Record<string, string | number>) => translate(locale, key, params), [locale]);
  return { locale, setLocale, t };
}

export const LocaleContext = createContext<LocaleApi | undefined>(undefined);

export function useLocale(): LocaleApi {
  const context = useContext(LocaleContext);
  if (context === undefined) throw new Error("useLocale must be used within a LocaleContext provider");
  return context;
}
